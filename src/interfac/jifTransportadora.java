    private void jtCodKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtCodKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            objtra = new transportadora();
            if (jtCod.getText().isEmpty()) {

                jifConsultaTransp objconsulta = new jifConsultaTransp();
                jPrincipal.jTela.add(objconsulta);
                objconsulta.setVisible(true);
                jPrincipal.jTela.setComponentZOrder(objconsulta, 0);

            } else {
                objtra.setId_transportadora(Integer.parseInt(jtCod.getText()));

                PESDAO = new PesquisaDAO();

                try {

                    if (PESDAO.PesquisarTelaTransportadora(objtra)) {
                        if ((objtra != null)) {
                            pesquisaTransportadora();

                        }
                    }
                } catch (SQLException erro) {
                    JOptionPane.showMessageDialog(this, "Erro ao pesquisar " + erro.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jtCodKeyPressed

