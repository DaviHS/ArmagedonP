    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed

        if (jrAtivo.isSelected()) {
            status = 1;
        }
        if (jrInativo.isSelected()) {
            status = 0;
        }

        int Cod;
        objtra = new transportadora();
        preencher_objetos();

        try {

            IEDAO = new IncluirEditarDAO();

            if (acao == 1) {

                jtQuem.setText("Cadastro Realizado por " + jtAcesso.getText());
                preencher_objetos();
                salvar();

                Cod = IEDAO.IncluirTransportadora(objtra);
                jtCod.setText(String.valueOf(Cod));
                JOptionPane.showMessageDialog(this, "Salvo com sucesso !");
            }

        } catch (SQLException error) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar: " + error.getMessage());
            cancelar();

        }
        if (acao == 2) {
            salvar();

            objtra.setId_transportadora(Integer.parseInt(jtCod.getText()));

            try {
                if (IEDAO.EditarTransportadora(objtra)) {

                    JOptionPane.showMessageDialog(this, "Alterado com Sucesso!");

                }
            } catch (SQLException ex) {
                System.err.println("Erro: " + ex);
            }

        }
    }//GEN-LAST:event_jbSalvarActionPerformed

