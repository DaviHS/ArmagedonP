    /**
     * Pesquisa endreço com base no cep inserido
     *
     * @param cep
     */
    public void buscarCep(String cep) {
        try {
            URL url = new URL("http://viacep.com.br/ws/" + cep + "/xml");
            SAXReader xml = new SAXReader();
            Document documento = xml.read(url);
            Element root = documento.getRootElement();

            for (Iterator<Element> it = root.elementIterator(); it.hasNext();) {
                Element element = it.next();

                // if (jtRuaT3.getText().isEmpty()){
                if (element.getQualifiedName().equals("logradouro")) {
                    jtRua.setText(element.getText().toUpperCase());
                }

                //  } if(jtBairroT3.getText().isEmpty()){
                if (element.getQualifiedName().equals("bairro")) {
                    jtBairro.setText(element.getText().toUpperCase());
                }
                // }

                //  if (jtCidadeT3.getText().isEmpty()){
                if (element.getQualifiedName().equals("localidade")) {
                    jtCidade.setText(element.getText().toUpperCase());
                }
                // }

                if (element.getQualifiedName().equals("uf")) {
                    jcUf.setSelectedItem(element.getText());
                }
                if (element.getQualifiedName().equals("ibge")) {
                    jtCodIbge.setText(element.getText());
                }

            }
        } catch (Exception e) {

            System.err.println("Erro: " + e);
        }

    }

    public ConsultaCNPJController getController() {
        if (controller == null) {
            controller = new ConsultaCNPJController();
        }
        return controller;
    }

    /**
     * preenche dados do CNPJ com base na informação inserida
     */
    public void buscarCNPJ() {
        String cnpj = jtCnpj.getText();
        cnpj = cnpj.replaceAll("[^0123456789]", "");
        try {
            PessoaJuridica pessoa = getController().consulta(cnpj);

            if (pessoa != null) {

                jcUf.setSelectedItem(pessoa.getEstado().toUpperCase());
                jtRazaoS.setText(pessoa.getRazaoSocial().toUpperCase());
                jtBairro.setText(pessoa.getBairro().toUpperCase());
                jtRua.setText(pessoa.getRua().toUpperCase());
                jtNum.setText(pessoa.getNumero());
                jtTelFixo.setText(pessoa.getTelefone());
                jtCep.setText(pessoa.getCep());
                jtCompl.setText(pessoa.getComplemento().toUpperCase());
                jtMailCon.setText(pessoa.getEnderecoEletronico());
                jtCidade.setText(pessoa.getMunicipio().toUpperCase());

                if (pessoa.getSituacao().equals("ATIVA")) {
                    jrAtivo.setSelected(true);
                } else {
                    jrInativo.setSelected(true);
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "CNPJ n�o encontrado.");
        }
    }
